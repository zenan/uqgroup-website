<p>Chimeric antigen receptors (CARs) are synthetic, fusion proteins composed of antibody-based extracellular domains linked to intracellular signaling domains typically derived from the natural T-cell receptor complex (e.g., CD3 zeta chain). CAR-modified T cells have shown exciting potential in treating otherwise intractable diseases such as relapsing leukemia and lymphoma, metastatic melanoma, and glioblastoma multiforme. </p>

<p>State-of-the-art CARs are single-input, single-output devices that execute a killing function in response to the detection of one antigen signal. As a result, CAR-based therapies are vulnerable to off-target toxicity toward healthy cells as well as mutational escape by tumor cells. Our laboratory is developing next-generation CARs that can perform logical computation of multiple input signals, thereby increasing the specificity as well as versatility of CAR-modified T cells for cancer therapy.</p>

